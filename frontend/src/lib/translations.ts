const STORAGE_KEY = 'careplus_locale';

export function getStoredLocale(): Locale {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v === 'ne' || v === 'en') return v;
  } catch {
    /* ignore */
  }
  return 'en';
}

export function setStoredLocale(locale: Locale): void {
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    /* ignore */
  }
}

/** Replace {{key}} in str with values from params */
export function interpolate(str: string, params: Record<string, string | number>): string {
  return str.replace(/\{\{(\w+)\}\}/g, (_, key) => String(params[key] ?? ''));
}
